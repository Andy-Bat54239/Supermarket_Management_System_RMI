    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String fullName = txtEmployeeName.getText().trim();
        String role = comboRole.getSelectedItem().toString();
        String username = txtUsername.getText().trim();
        String salaryStr = txtSalary.getText().trim();
        String contact = txtContact.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        
        ValidationResult salaryValidation = ValidationUtil.validateNumeric(salaryStr, "Salary");
        if (!ValidationUtil.validateAndShow(this, salaryValidation)) {
            txtSalary.requestFocus();
            return;
        }
        
        double salary = Double.parseDouble(salaryStr);
        
        ValidationResult validation = ValidationUtil.validateEmployee(
            fullName, role, username, salary, contact, password
        );
        
        if (!ValidationUtil.validateAndShow(this, validation)) {
            return;
        }
        
        try {
            Employee employee = new Employee();
            employee.setFullName(fullName);
            employee.setRole(role);
            employee.setUsername(username);
            employee.setSalary(salary);
            employee.setContact(contact);
            employee.setPasswordHash(password); 
            
            Integer employeeId = employeeService.addEmployee(employee);
            
            if (employeeId != null && employeeId > 0) {
                ValidationUtil.showSuccess(this,
                    "Employee added successfully!\n\n" +
                    "Employee ID: " + employeeId + "\n" +
                    "Name: " + fullName + "\n" +
                    "Username: " + username);
                
                retrieveDataInTable();
                clearFields();
                
                // Refresh employee list in SalesPanel if available
                if (panel != null) {
                    panel.loadEmployeeNameToCombo();
                }
            } else {
                ValidationUtil.showError(this,
                    "Failed to add employee. Please try again.",
                    "Operation Failed");
            }
        } catch (Exception e) {
            ValidationUtil.showError(this,
                "Error adding employee: " + e.getMessage(),
                "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String searchStr = txtSearch.getText().trim();
        
        ValidationResult idValidation = ValidationUtil.validateInteger(searchStr, "Employee ID");
        if (!ValidationUtil.validateAndShow(this, idValidation)) {
            ValidationUtil.showWarning(this,
                "Please search for an employee first using the Search field",
                "Search Required");
            txtSearch.requestFocus();
            return;
        }
        
        String fullName = txtEmployeeName.getText().trim();
        String role = comboRole.getSelectedItem().toString();
        String username = txtUsername.getText().trim();
        String salaryStr = txtSalary.getText().trim();
        String contact = txtContact.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        
        ValidationResult salaryValidation = ValidationUtil.validateNumeric(salaryStr, "Salary");
        if (!ValidationUtil.validateAndShow(this, salaryValidation)) {
            txtSalary.requestFocus();
            return;
        }
        
        double salary = Double.parseDouble(salaryStr);
        
        ValidationResult validation = ValidationUtil.validateEmployee(
            fullName, role, username, salary, contact, password
        );
        
        if (!ValidationUtil.validateAndShow(this, validation)) {
            return;
        }
        
        int employeeId = Integer.parseInt(searchStr);
        
        try {
            Employee existingEmployee = employeeService.findEmployeeById(employeeId);
            
            if (existingEmployee == null) {
                ValidationUtil.showError(this,
                    "Employee not found with ID: " + employeeId,
                    "Not Found");
                return;
            }
            
            existingEmployee.setFullName(fullName);
            existingEmployee.setRole(role);
            existingEmployee.setUsername(username);
            existingEmployee.setSalary(salary);
            existingEmployee.setContact(contact);
            existingEmployee.setPasswordHash(password);
            
            boolean updated = employeeService.updateEmployee(existingEmployee);
            
            if (updated) {
                ValidationUtil.showSuccess(this,
                    "Employee updated successfully!\n\n" +
                    "Employee: " + fullName + "\n" +
                    "Role: " + role + "\n" +
                    "Salary: RWF " + String.format("%,.2f", salary));
                
                retrieveDataInTable();
                clearFields();
            } else {
                ValidationUtil.showError(this,
                    "Failed to update employee. Please try again.",
                    "Update Failed");
            }
        } catch (Exception e) {
            ValidationUtil.showError(this,
                "Error updating employee: " + e.getMessage(),
                "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchStr = txtSearch.getText().trim();
        
        ValidationResult idValidation = ValidationUtil.validateInteger(searchStr, "Employee ID");
        if (!ValidationUtil.validateAndShow(this, idValidation)) {
            txtSearch.requestFocus();
            return;
        }
        
        int employeeId = Integer.parseInt(searchStr);
        
        try {
            Employee foundEmployee = employeeService.findEmployeeById(employeeId);
            
            if (foundEmployee != null) {
                txtEmployeeName.setText(foundEmployee.getFullName());
                comboRole.setSelectedItem(foundEmployee.getRole());
                txtContact.setText(foundEmployee.getContact());
                txtSalary.setText(String.valueOf(foundEmployee.getSalary()));
                txtUsername.setText(foundEmployee.getUsername());
                txtPassword.setText("");
                
            } else {
                ValidationUtil.showWarning(this,
                    "Employee not found with ID: " + employeeId,
                    "Not Found");
            }
        } catch (Exception e) {
            ValidationUtil.showError(this,
                "Error searching employee: " + e.getMessage(),
                "Search Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Get selected row from table
        int selectedRow = employeeTable.getSelectedRow();
        
        if (selectedRow == -1) {
            ValidationUtil.showWarning(this,
                "Please select an employee from the table to delete",
                "No Selection");
            return;
        }
        
        // Get employee ID from selected row (column 0)
        int employeeId = (int) employeeTable.getValueAt(selectedRow, 0);
        String employeeName = (String) employeeTable.getValueAt(selectedRow, 1);
        String employeeRole = (String) employeeTable.getValueAt(selectedRow, 2);
        
        // Check if trying to delete self
        if (employeeId == currentEmployee.getEmployeeId()) {
            ValidationUtil.showWarning(this,
                "You cannot delete your own account!\n\n" +
                "Please ask another administrator to delete this account if needed.",
                "Cannot Delete Self");
            return;
        }
        
        try {
            // Show delete confirmation with employee details
            int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this employee?\n\n" +
                "Employee: " + employeeName + "\n" +
                "Role: " + employeeRole + "\n" +
                "ID: " + employeeId + "\n\n" +
                "This action cannot be undone!",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
            
            if (confirm != JOptionPane.YES_OPTION) {
                return; // User cancelled
            }
            
            // Delete employee
            boolean deleted = employeeService.deleteEmployee(employeeId);
            
            if (deleted) {
                ValidationUtil.showSuccess(this,
                    "Employee deleted successfully!\n\n" +
                    "Employee: " + employeeName);
                
                retrieveDataInTable();
                clearFields();
            } else {
                ValidationUtil.showError(this,
                    "Failed to delete employee.\n\n" +
                    "Possible reasons:\n" +
                    "• Employee has related sales records\n" +
                    "• Employee has inventory transactions\n" +
                    "• Database constraint violation\n\n" +
                    "Consider deactivating the employee instead.",
                    "Delete Failed");
            }
        } catch (Exception e) {
            ValidationUtil.showError(this,
                "Error deleting employee: " + e.getMessage(),
                "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void profileBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_profileBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = employeeTable.getSelectedRow();
        
        if(selectedRow == -1){
            ValidationUtil.showError(this, "Please select an employee from the table to view their profile", "No Employee Selected");
            return;
        }
        
        try{
            int employeeId = (int) employeeTable.getValueAt(selectedRow, 0);
            String employeeName = (String) employeeTable.getValueAt(selectedRow, 1);
            Employee selectedEmployee = employeeService.findEmployeeById(employeeId);
            
            if(selectedEmployee == null){
                ValidationUtil.showError(this,
                        "Employee data not found in database!\n\n" +
                        "Employee ID: " + employeeId + "\n" +
                        "Name: " + employeeName,
                        "Data Not found");
                return;
            }
            
            Frame parentFrame = (Frame) SwingUtilities.getWindowAncestor(this);
            EmployeeProfilePanel profilePanel = new EmployeeProfilePanel(parentFrame, selectedEmployee, false);
            
            profilePanel.setVisible(true);
            retrieveDataInTable();
        } catch(Exception e){
            ValidationUtil.showError(this, "Error opening employee profile:\n\n" + e.getMessage(), "Profile Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_profileBtnActionPerformed


