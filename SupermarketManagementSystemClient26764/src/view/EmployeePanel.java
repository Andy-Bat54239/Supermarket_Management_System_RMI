    private void constructTableColumns(){
        tblModel.addColumn("Customer ID");
        tblModel.addColumn("Employee Name");
        tblModel.addColumn("Role");
        tblModel.addColumn("Salary");
        tblModel.addColumn("Hire Date");
        tblModel.addColumn("Contact");
        employeeTable.setModel(tblModel);
    }
    
    private void retrieveDataInTable(){
        tblModel.setRowCount(0);
        
        try{
            List<Employee> employees = employeeService.findAllEmployees();
            
            if(employees != null){
                for(Employee employee : employees){
                    tblModel.addRow(new Object[]{
                        employee.getEmployeeId(),
                        employee.getFullName(),
                        employee.getRole(),
                        employee.getSalary(),
                        employee.getHireDate(),
                        employee.getContact()
                    });
                }
            }
        } catch(Exception e){
            ValidationUtil.showError(this,
                "Error loading employees: " + e.getMessage(),
                "Database Error");
            e.printStackTrace();
        }
    }
    
