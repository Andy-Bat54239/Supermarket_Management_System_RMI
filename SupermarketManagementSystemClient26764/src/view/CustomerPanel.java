    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchStr = txtSearch.getText().trim();
        
        ValidationResult idValidation = ValidationUtil.validateInteger(searchStr, "Customer ID");
        if(!ValidationUtil.validateAndShow(this, idValidation)){
            txtSearch.requestFocus();
            return;
        }
        
        int customerId = Integer.parseInt(searchStr);
        
        try{
            Customer foundCustomer = customerService.findCustomerById(customerId);
            
            if(foundCustomer != null){
                txtCustomerName.setText(foundCustomer.getFullName());
                txtEmail.setText(foundCustomer.getEmail());
                txtPhoneNumber.setText(foundCustomer.getPhone());
                txtAddress.setText(foundCustomer.getAddress());
            } else {
                ValidationUtil.showWarning(this, "Customer not found with ID: "+ customerId, "Not Found");
            }
        }catch(Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

