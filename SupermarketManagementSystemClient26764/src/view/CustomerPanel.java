    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String fullName = txtCustomerName.getText().trim();
        String email = txtEmail.getText().trim();
        String phone = txtPhoneNumber.getText().trim();
        String address = txtAddress.getText().trim();
        
        ValidationResult validation = ValidationUtil.validateCustomer(fullName, email, phone, address);
        
        if(!ValidationUtil.validateAndShow(this, validation)){
            return;
        }
        
        try{
            Customer customer = new Customer();
            customer.setFullName(fullName);
            customer.setEmail(email);
            customer.setPhone(phone);
            customer.setAddress(address);
            
            Integer customerId = customerService.addCustomer(customer);
            if(customerId != null && customerId > 0){
                ValidationUtil.showSuccess(this, "Customer added successfully!\n\n" + "Customer ID: "+ customerId + "\nName: "+ fullName);
                retrieveDataFromDB();
                refresh();
                
                if(salesPanel != null){
                    salesPanel.loadCustomerIdToCombo();
                }
            } else{
                ValidationUtil.showError(this, "Failed to add Customer. Please try again.", "Operation Failed");
            }
        } catch(Exception e){
            ValidationUtil.showError(this, "Error adding customer: "+ e.getMessage(), "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

