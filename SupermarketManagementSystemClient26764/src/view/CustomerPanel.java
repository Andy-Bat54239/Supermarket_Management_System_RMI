    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String searchStr = txtSearch.getText().trim();
        
        ValidationResult idValidation = ValidationUtil.validateInteger(searchStr, "Customer ID");
        if(!ValidationUtil.validateAndShow(this, idValidation)){
            ValidationUtil.showWarning(this, "Please search for a customer first using the Search field", "Search Required");
            txtSearch.requestFocus();
            return;
        }
        
        String fullName = txtCustomerName.getText().trim();
        String email = txtEmail.getText().trim();
        String phone = txtPhoneNumber.getText().trim();
        String address = txtAddress.getText().trim();
        
        ValidationResult validation = ValidationUtil.validateCustomer(fullName, email, phone, address);
        
        if(!ValidationUtil.validateAndShow(this, validation)){
            return;
        }
        
        int customerId = Integer.parseInt(searchStr);
        
        try{
            Customer existingCustomer = customerService.findCustomerById(customerId);
            
            if(existingCustomer == null){
                ValidationUtil.showError(this, "Customer not found with ID: " + customerId, "Not Found");
                return;
            }
            
            existingCustomer.setFullName(fullName);
            existingCustomer.setEmail(email);
            existingCustomer.setPhone(phone);
            existingCustomer.setAddress(address);
            
            boolean updated = customerService.updateCustomer(existingCustomer);
            
            if(updated){
                ValidationUtil.showSuccess(this,
                        "Customer updated successfully!\n\n"+
                        "Customer: " + fullName + "\n" +
                        "Email: " + email + "\n" +
                        "Phone: " + phone);
                retrieveDataFromDB();
                refresh();
            } else{
                ValidationUtil.showError(this,
                        "Failed to Update customer. Please try again",
                        "Update Failed");
            }
            
        } catch(Exception e){
            ValidationUtil.showError(this,
                    "Error updating customer: " + e.getMessage(),
                    "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(isCashierMode){
            ValidationUtil.showWarning(this, "Access Denied\n\nOnly Admins can delete customers.", "Permission Denied");
            return;
        }
        
        // Get selected row from table
        int selectedRow = customerTable.getSelectedRow();
        
        if (selectedRow == -1) {
            ValidationUtil.showWarning(this,
                "Please select a customer from the table to delete",
                "No Selection");
            return;
        }
        
        // Get customer ID from selected row (column 0)
        int customerId = (int) customerTable.getValueAt(selectedRow, 0);
        String customerName = (String) customerTable.getValueAt(selectedRow, 1);
        
        try {
            // Show delete confirmation with customer details
            int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to delete this customer?\n\n" +
                "Customer: " + customerName + "\n" +
                "ID: " + customerId + "\n\n" +
                "This action cannot be undone!",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE);
            
            if (confirm != JOptionPane.YES_OPTION) {
                return;
            }
            
            // Delete customer
            boolean deleted = customerService.deleteCustomer(customerId);
            
            if (deleted) {
                ValidationUtil.showSuccess(this, 
                    "Customer deleted successfully!\n\n" +
                    "Customer: " + customerName);
                
                retrieveDataFromDB();
                refresh();
            } else {
                ValidationUtil.showError(this, 
                    "Failed to delete customer.\n\n" +
                    "Possible reasons:\n" +
                    "• Customer has related sales records\n" +
                    "• Database constraint violation",
                    "Delete Failed");
            }
        } catch (Exception e) {
            ValidationUtil.showError(this, 
                "Error deleting customer: " + e.getMessage(),
                "Server Error");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


